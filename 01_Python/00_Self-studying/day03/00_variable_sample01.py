#  변수의 생성 및 할당
pi = 3.14159265; # 3.14159265 이라는 value를 pi라는 저장 공간의 주소값을 따라서 값을 할당 해준다.
print(pi) 
# pi로 할당 된 value는 pi라는 저장 공간에 있는거고 print 출력 함수는 pi의 값을 호출 한다. 이때  3.14159265의 value는 pi에 저장이 되어 있기 때문에 print가 출력 할 때, 그대로 주소 값을 따라서 출력이 됩니다.

# ex)
pi = 3.14159265;
print(pi + 2)

pi = 3.14159265
print(pi-2)

pi = 3.14159265
print(pi*2)

pi = 3.14159265
print(pi/2)

pi = 3.14159265
print(pi//2)

pi = 3.14159265
print(pi%2)

pi = 3.14159265
print(pi**2)
pi = 3.14159265
print(pi*pi)
